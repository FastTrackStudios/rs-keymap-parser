#[cfg(test)]
mod tests {
    use crate::keycodes::KeyCode;
    #[test]
    fn test_w() {
        let w = KeyCode::W.as_u8();
        println!("{}", w);
        assert_eq!(w, 87);
    }

    #[test]
    fn test_u8_to_keycode() {
        KeyCode::from_u16(87);
        assert_eq!(KeyCode::W.as_u8(), 87);
    }
}
